         <div id="Avantages">
            <h4>Avantages</h4>
            <p>L’alternance est une voie privilégiée pour construire un avenir solide. La preuve : près de 100% des apprentis ont un emploi trois mois après leur diplôme.
               Un salaire motivant et des frais d’études payés
               Les étudiants n’ont rien à payer. Les frais d’inscription sont pris en charge par le CFA AFIA et la formation par l’entreprise d’accueil.
               Les apprentis ne sont pas imposables jusqu’au SMIC annuel
               La rémunération mensuelle des apprentis est calculée sur la base d’un pourcentage du SMIC ou du SMC (Salaire minimum conventionnel). Ils sont également exonérés de charges sociales dans la limite de 79% du SMIC.
            </p>
            <p><strong>Une formation à la fois pratique et théorique</strong><br>
               Les mêmes cours dispensés par les mêmes professeurs des universités. C’est une professionnalisation en alliant la théorie des cours et la pratique en entreprise. Le rythme d’alternance permet l’application des concepts dans la réalité.
            </p>
         </div>
         <p><strong>Le statut salarié</strong><br>
            L’apprenti bénéficie du statut de salarié à plein temps, avec ses avantages (exemples : congés, tickets restaurant, RTT, mutuelle, etc.), réparti entre l’université et l’entreprise. Ce n’est ni un stagiaire, ni un étudiant (il bénéficie tout de même de la carte d’étudiant).
